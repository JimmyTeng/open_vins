static constexpr int CHI2_QUANTILE_095_SIZE = sizeof(CHI2_QUANTILE_095) / sizeof(CHI2_QUANTILE_095[0]);

/**
 * @brief 获取卡方分布 0.95 分位数
 * @param df 自由度 (degrees of freedom), 需满足 df >= 1
 * @return 0.95 置信度下的卡方临界值。df<=999 用查表，df>999 用 Wilson-Hilferty 近似
 */
inline double chi2_quantile_095(int df) {
  if (df >= 1 && df <= CHI2_QUANTILE_095_SIZE) {
    return CHI2_QUANTILE_095[df - 1];
  }
  // df > 999: Wilson-Hilferty 近似
  constexpr double Z_095 = 1.6448536269514722;
  double n = static_cast<double>(df);
  double t = 1.0 - 2.0 / (9.0 * n) + Z_095 * std::sqrt(2.0 / (9.0 * n));
  return n * (t * t * t);
}

